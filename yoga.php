      <h1 ><a name="benefits">Health Benefits</h1>
      <p class="w3-text-grey">What Are The Health Benefits?
Yoga comes with a host of physical and mental benefits. The postures, known as asanas, stretch and flex various muscle groups. Here are a few reasons why you should take up yoga:</p>
<p class="w3-text-grey">
Makes You Flexible: 
When your hip muscles tighten, it can strain the knee joint because the thigh and shinbones are improperly aligned. Also, tight hamstrings gradually flatten the lumbar spine, leading to back pain. If the muscles and connective tissues are not flexible, it results in poor posture. Yoga solves all of these problems. It gradually loosens stiffness in muscles and joints, improving flexibility. At the same time, it reduces body aches and joint pains.
</p><p class="w3-text-grey">
Builds Muscle Strength: 
When your muscles are stronger, they protect you from arthritis and back pain, among others. Yoga helps build muscle strength, but unlike lifting weights at gyms, it doesn't do that at the expense of flexibility. So strength building in yoga goes hand in hand with flexibility.
</p>
<p class="w3-text-grey">
Improves Posture: 
Improper posture can lead to back, neck and other muscle and joint problems. When you slump, your body compensates it by flattening the normal inward curves in your neck and lower back. This causes pain and degenerative arthritis of the spine. The asanas in yoga help you maintain these optimal curves, thereby improving posture. 
</p> <p class="w3-text-grey">
Prevents Cartilage And Joint Breakdown: 
When you do yoga, the joints go through their full range of motion. This helps prevent degenerative arthritis and mitigate disability by utilising parts of the cartilage that are normally not used. Joint cartilage receives fresh nutrients only when its fluid is squeezed out and a new supply can be soaked up. Without proper sustenance, neglected areas of the cartilage can eventually wear out, exposing the underlying bone.
</p><p class="w3-text-grey">
Improves Bone Health: 
A lot of postures in yoga require you to use your body weight. They help strengthen the bones, especially the bones in your arms which are vulnerable to osteoporotic fractures. According to a study by California State University, Los Angeles, practicing yoga increases bone density in the vertebrae.
</p>
<p class="w3-text-grey">
Increases Blood Flow: 
The exercises in yoga help you relax and gradually increase blood circulation, especially in your hands and feet. Yoga also pumps more oxygen to your cells, which function better consequently. The twisting poses wring out venous blood from the internal organs and allow oxygenated blood to flow in once the twist is released. Inverted poses like headstand, handstand and shoulder stand encourage venous blood from the legs and pelvis to flow back to the heart. Yoga also boosts levels of haemoglobin and red blood cells, which carry oxygen to the tissues.
</p>
